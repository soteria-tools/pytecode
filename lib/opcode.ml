  | LIST_APPEND | LIST_EXTEND | LOAD_ATTR | LOAD_CONST
  | LOAD_DEREF | LOAD_FAST | LOAD_FAST_AND_CLEAR | LOAD_FAST_CHECK
  | LOAD_FAST_LOAD_FAST | LOAD_FROM_DICT_OR_DEREF | LOAD_FROM_DICT_OR_GLOBALS | LOAD_GLOBAL
